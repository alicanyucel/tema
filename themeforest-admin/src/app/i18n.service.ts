@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly document = inject(DOCUMENT);

  readonly language = signal<AppLanguage>(this.getInitialLanguage());

  t(key: string): string {
    const lang = this.language();
    return TRANSLATIONS[lang][key] ?? TRANSLATIONS.tr[key] ?? key;
  }

  setLanguage(lang: AppLanguage): void {
    this.language.set(lang);
    if (typeof localStorage !== 'undefined') {
      localStorage.setItem('atlas-lang', lang);
    }
    this.document.documentElement.lang = lang;
  }

  private getInitialLanguage(): AppLanguage {
    const saved = typeof localStorage !== 'undefined' ? localStorage.getItem('atlas-lang') : null;
    if (saved === 'tr' || saved === 'en') {
      this.document.documentElement.lang = saved;
      return saved;
    }

    this.document.documentElement.lang = 'tr';
    return 'tr';
  }
}
